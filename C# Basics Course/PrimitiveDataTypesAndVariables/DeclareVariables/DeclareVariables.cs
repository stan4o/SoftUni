using System;

class DeclareVariables
{
    static void Main()
    {
        ushort ushortVar = 52130;
        sbyte sbyteVar = -115;
        uint uintVar = 4825932;
        byte byteVar = 97;
        int intVar = -10000;

        Console.WriteLine(ushortVar);
        Console.WriteLine(sbyteVar);
        Console.WriteLine(uintVar);
        Console.WriteLine(byteVar);
        Console.WriteLine(intVar);
    }
}
